export async function seedKnowledge(db: PrismaClient) {
  resetCounter()

  const chapters = await db.chapter.findMany({
    where: { isActive: true, deletedAt: null },
    include: { subject: true },
    orderBy: [{ subjectId: 'asc' }, { order: 'asc' }],
  })

  for (const ch of chapters) {
    const existingCount = await db.knowledgeQuestion.count({ where: { chapterId: ch.id, deletedAt: null } })
    const toCreate = Math.max(0, 4 - existingCount)
    if (toCreate === 0) continue

    for (let i = 0; i < toCreate; i++) {
      const isKnowledge = i < 2
      const template = isKnowledge
        ? KNOWLEDGE_TEMPLATES[i % KNOWLEDGE_TEMPLATES.length]
        : COMPREHENSION_TEMPLATES[i % COMPREHENSION_TEMPLATES.length]
      const isPremium = i >= 3

      await db.knowledgeQuestion.create({
    data: {
      id: deterministicId('kq'),
      chapterId: ch.id,
      type: isKnowledge ? 'knowledge' : 'comprehension',
      question: template.q,
      answer: template.a,
      order: i + 1,
      price: isPremium ? 5 : 0,
    },
  })
    }
  }
}
